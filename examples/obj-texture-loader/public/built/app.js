  var Mesh = class {
    constructor({ shaders, name, locationNames, parameters, gl: gl2 }) {
      this.readyToRender = true;
      this.buffers = {};
      this.geometry = {};
      this.locations = { attributes: {}, uniforms: {} };
      this.textures = [];
      this.modelMatrix = mat4_exports.create();
      this.projectionMatrix = mat4_exports.create();
      this.position = vec3_exports.fromValues(0, 0, 0);
      this.rotation = vec3_exports.fromValues(0, 0, 0);
      this.scale = vec3_exports.fromValues(1, 1, 1);
      this.onBeforeDrawCallbacks = [];
      this.onDrawCallbacks = [];
      this.static = false;
      this.addOnDrawCallback = (callback) => {
        this.onDrawCallbacks.push(callback);
      };
      this.addOnBeforeDrawCallback = (callback) => {
        this.onBeforeDrawCallbacks.push(callback);
      };
      // Set a 2D texture
      this.loadTexture = (gl2, path, uniform = "uTexture", options) => new Promise((resolve) => {
        const texture = gl2.createTexture();
        this.textures.push(texture);
        const image = new Image();
        image.src = path;
        image.onload = () => {
          options.flip && gl2.pixelStorei(gl2.UNPACK_FLIP_Y_WEBGL, 1);
          gl2.bindTexture(gl2.TEXTURE_2D, texture);
          gl2.texParameteri(gl2.TEXTURE_2D, gl2.TEXTURE_MIN_FILTER, options.minFilter);
          gl2.texParameteri(gl2.TEXTURE_2D, gl2.TEXTURE_MAG_FILTER, options.magFilter);
          gl2.texParameteri(gl2.TEXTURE_2D, gl2.TEXTURE_WRAP_S, options.wrapS);
          gl2.texParameteri(gl2.TEXTURE_2D, gl2.TEXTURE_WRAP_T, options.wrapT);
          gl2.texImage2D(gl2.TEXTURE_2D, 0, gl2.RGB, gl2.RGB, gl2.UNSIGNED_BYTE, image);
          options.mipmap && gl2.generateMipmap(gl2.TEXTURE_2D);
          resolve();
        };
      });
      this.calcMatrixes = (gl2) => {
        {
          const glCanvas = gl2.canvas;
          const fieldOfView = 45 * Math.PI / 180;
          const aspect = glCanvas.clientWidth / glCanvas.clientHeight;
          const zNear = 0.1;
          const zFar = 100;
          mat4_exports.perspective(
            this.projectionMatrix,
            fieldOfView,
            aspect,
            zNear,
            zFar
          );
        }
        mat4_exports.identity(this.modelMatrix);
        mat4_exports.scale(
          this.modelMatrix,
          this.modelMatrix,
          [this.scale[0], this.scale[1], this.scale[2]]
        );
        mat4_exports.translate(
          this.modelMatrix,
          this.modelMatrix,
          [this.position[0], this.position[1], this.position[2]]
        );
        mat4_exports.rotate(
          this.modelMatrix,
          this.modelMatrix,
          this.rotation[0],
          // amount to rotate in radians
          [1, 0, 0]
        );
        mat4_exports.rotate(
          this.modelMatrix,
          // destination matrix
          this.modelMatrix,
          this.rotation[1],
          // amount to rotate in radians
          [0, 1, 0]
        );
        mat4_exports.rotate(
          this.modelMatrix,
          this.modelMatrix,
          this.rotation[2],
          // amount to rotate in radians
          [0, 0, 1]
        );
        gl2.uniformMatrix4fv(
          this.locations.uniforms.uProjectionMatrix,
          false,
          this.projectionMatrix
        );
        gl2.uniformMatrix4fv(
          this.locations.uniforms.uModelMatrix,
          false,
          this.modelMatrix
        );
      };
      this.getAttributesFromBuffers = (gl2) => {
        const { positions, indices, colors, uvs, normals } = this.buffers;
        if (positions && this.locations.attributes.aPosition > -1) {
          {
            gl2.bindBuffer(gl2.ARRAY_BUFFER, this.buffers.positions);
            const index = this.locations.attributes.aPosition;
            const numComponents = 3;
            const type = gl2.FLOAT;
            const normalize2 = false;
            const stride = 0;
            const offset = 0;
            gl2.vertexAttribPointer(
              index,
              numComponents,
              type,
              normalize2,
              stride,
              offset
            );
            gl2.enableVertexAttribArray(index);
          }
        }
        if (uvs && this.locations.attributes.aUv > -1) {
          {
            gl2.bindBuffer(gl2.ARRAY_BUFFER, this.buffers.uvs);
            const index = this.locations.attributes.aUv;
            const size = 2;
            const type = gl2.FLOAT;
            const normalize2 = false;
            const stride = 0;
            const offset = 0;
            gl2.enableVertexAttribArray(index);
            gl2.vertexAttribPointer(index, size, type, normalize2, stride, offset);
          }
        }
        if (colors && this.locations.attributes.aColor > -1) {
          {
            const index = this.locations.attributes.aColor;
            const size = 4;
            const type = gl2.FLOAT;
            const normalize2 = false;
            const stride = 0;
            const offset = 0;
            gl2.bindBuffer(gl2.ARRAY_BUFFER, this.buffers.colors);
            gl2.vertexAttribPointer(
              index,
              size,
              type,
              normalize2,
              stride,
              offset
            );
            gl2.enableVertexAttribArray(index);
          }
        }
        if (normals && this.locations.attributes.aNormal > -1) {
          {
            const index = this.locations.attributes.aNormal;
            const size = 3;
            const type = gl2.FLOAT;
            const normalize2 = false;
            const stride = 0;
            const offset = 0;
            gl2.bindBuffer(gl2.ARRAY_BUFFER, this.buffers.normals);
            gl2.vertexAttribPointer(
              index,
              size,
              type,
              normalize2,
              stride,
              offset
            );
            gl2.enableVertexAttribArray(index);
          }
        }
        if (indices) {
          gl2.bindBuffer(gl2.ELEMENT_ARRAY_BUFFER, this.buffers.indices);
        }
      };
      this.setBuffers = (gl2) => {
        const { positions, indices, colors, uvs, normals } = this.geometry;
        if (positions) {
          this.setBuffer(gl2, "positions");
        }
        if (colors) {
          this.setBuffer(gl2, "colors");
        }
        if (uvs) {
          this.setBuffer(gl2, "uvs");
        }
        if (normals) {
          this.setBuffer(gl2, "normals");
        }
        if (indices) {
          const indexBuffer = gl2.createBuffer();
          gl2.bindBuffer(gl2.ELEMENT_ARRAY_BUFFER, indexBuffer);
          gl2.bufferData(gl2.ELEMENT_ARRAY_BUFFER, indices, gl2.STATIC_DRAW);
          this.buffers.indices = indexBuffer;
        }
      };
      this.setBuffer = (gl2, nameProp) => {
        const buffer = gl2.createBuffer();
        gl2.bindBuffer(gl2.ARRAY_BUFFER, buffer);
        gl2.bufferData(gl2.ARRAY_BUFFER, this.geometry[nameProp], gl2.STATIC_DRAW);
        this.buffers[nameProp] = buffer;
      };
      // LINE_LOOP for wireframe-like aspect
      this.draw = (gl2, mode, deltaTime, elapsedTime) => {
        for (const callback of this.onBeforeDrawCallbacks) {
          callback(this, deltaTime, elapsedTime);
        }
        if (typeof this.geometry.indices !== "undefined" && this.geometry.indices.length && this.geometry.positions) {
          const vertexCount = this.geometry.positions.length / (this.geometry.positions.length / this.geometry.indices.length);
          const type = gl2.UNSIGNED_SHORT;
          const offset = 0;
          gl2.drawElements(mode, vertexCount, type, offset);
        } else if (this.geometry.positions) {
          gl2.bindBuffer(gl2.ARRAY_BUFFER, this.buffers.positions);
          gl2.enableVertexAttribArray(this.locations.attributes.aPosition);
          {
            const size = 3;
            const type = gl2.FLOAT;
            const normalize2 = false;
            const stride = 0;
            const offset = 0;
            gl2.vertexAttribPointer(
              this.locations.attributes.aPosition,
              size,
              type,
              normalize2,
              stride,
              offset
            );
          }
          {
            const primitiveType = gl2.TRIANGLES;
            const offset = 0;
            const count = this.geometry.positions.length / 3;
            gl2.drawArrays(primitiveType, offset, count);
          }
        }
        for (const callback of this.onDrawCallbacks) {
          callback(this, deltaTime, elapsedTime);
        }
      };
      this.program = getShaderProgram(gl2, shaders[0], shaders[1]);
      for (const attributeName of locationNames.attributes) {
        this.locations.attributes[attributeName] = gl2.getAttribLocation(this.program, attributeName);
      }
      for (const uniformName of locationNames.uniforms) {
        this.locations.uniforms[uniformName] = gl2.getUniformLocation(this.program, uniformName);
      }
      if (parameters) {
        if (parameters.position)
          vec3_exports.set(this.position, parameters.position.x, parameters.position.y, parameters.position.z);
        if (parameters.rotation)
          vec3_exports.set(this.rotation, parameters.rotation.x, parameters.rotation.y, parameters.rotation.z);
        if (parameters.scale)
          vec3_exports.set(this.scale, parameters.scale.x, parameters.scale.y, parameters.scale.z);
      }
      name ? this.name = name : this.name = "";
    }
  };

